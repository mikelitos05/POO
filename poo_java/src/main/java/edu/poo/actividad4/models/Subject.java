package edu.poo.actividad4.models;

public class Subject {

    private String name;
    private int credits;
    private int hours;

    public Subject(String name, int credits, int hours) {
        this.name = name;
        this.credits = credits;
        this.hours = hours;
    }

    public String getName() {
        return name;
    }

    public int getCredits() {
        return credits;
    }

    public int getHours() {
        return hours;
    }







}
